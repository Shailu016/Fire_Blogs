    {
        $report = Report::where('user_id', Auth::id())->where('comment_id', $comments->id )->first();

        if(!$report){

            $report = Report::create([
              
             'user_id' => Auth::id(),
             'comment_id' =>  $comments->id
    
           ]);
    
           return $report;
    
        }else{
            $report->delete();
            return "report delete";
        }
        
    
    }

